#include <stdio.h>
#include <stdlib.h>

int main()
{
    int num1,num2;

    printf("Enter the first value = ");
    scanf("%d",&num1);

    printf("Enter the second value = ");
    scanf("%d",&num2);

    printf("%d + %d = %d\n", num1,num2,num1+num2);

    printf("%d - %d = %d\n", num1,num2,num1-num2);

    printf("%d * %d = %d\n", num1,num2,num1*num2);

    printf("%d / %d = %d\n", num1,num2,num1/num2);
    return 0;
}
